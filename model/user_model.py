class User:
    def __init__(self, name, email, image, password):
        self.name = name
        self.email = email
        self.image = image
        self.password = password

    def get_name(self):
        self.name = name

    def set_name(self):
        return self.name

    def get_email(self):
        self.email = email

    def set_email(self):
        return self.email

    def get_image(self):
        self.image = image

    def set_image(self):
        return self.image

    def get_password(self):
        self.password = password

    def set_password(self):
        return self.password

